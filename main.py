#Maria - romb
def romb(x,color1,fillcolor1):
    down()
    color(color1)
    begin_fill()
    fillcolor(fillcolor1)
    for _ in range (2):
        forward(x)
        right(135)
        forward(x)
        right(45)
    end_fill()
    up()
#Yana - triangle
def triangle (x,color2,fillcolor2):
    down()
    color(color2)
    begin_fill()
    fillcolor(fillcolor2)
    forward (x)
    left (90)
    forward (x)
    left (135)
    forward (sqrt(2*x**2))
    end_fill()
    up()
